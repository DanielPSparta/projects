a = 10
b = 3
#Operators in python
c = a + b
print("Addition: ",c)

c = a - b
print("Subtraction: ",c)

c = a*b
print("Multiplication: ",c)

c = a/b
print("Division: ",c)

#modulo and remainder, exponential

c = a % b       # remainder
print("Modulo: ",c)

c = a // b      # floor division
print("floor division: ",c)

c = a ** b      # exponential
print("exponential: ",c)
